package operators;

public class AssignmentOperators {

	public static void main(String[] args) {
		
		int a = 20;
		int b = 10;
	
		 a+=b;
		 System.out.println("value a "+a);//Plus equal to(a+=b ;a = a+b)
		 System.out.println("value b "+b);
		 
		 a-=b;
		 System.out.println("value a "+a);//Minus equal to(a-=b ;a = a-b)
		 System.out.println("value b "+b);
		 
		 a*=b;
		 System.out.println("value a "+a);//Minus equal to(a*=b ;a = a*b)
		 System.out.println("value b "+b);
		 
		 a/=b;
		 System.out.println("value a "+a);//Divide equal to(a/=b ;a = a/b)
		 System.out.println("value b "+b);
		 
		 a%=b;   
		 System.out.println("value a "+a);//Divide equal to(a/=b ;a = a/b)
		 System.out.println("value b "+b);
		 

	}

}
